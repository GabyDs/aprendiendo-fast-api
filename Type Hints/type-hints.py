# dynamic typing

my_string_variable = "My String variable"
print(my_string_variable)
print(type(my_string_variable))

my_string_variable = 5
print(my_string_variable)
print(type(my_string_variable))

# defining the typing

my_typed_string: str = "My typed String variable"
print(my_typed_string)
print(print(my_typed_string))

my_typed_string: int = 5
print(my_typed_string)
print(print(my_typed_string))
